<p style="text-align: justify;">
Fintech Association for Consumer Empowerment (“FACE”)
<br><br>
Ethical Code of Conduct (“Code of Conduct”)
<br><br>
<b>INTRODUCTION AND APPLICATION</b>
<br><br><b>
PURPOSE
</b><br><br>
India is fast moving towards becoming a digital economy, with technology being its main accelerating factor helping in speeding up and growth of industries and market. However, this speed can also result in growth of certain unwanted dodgy practices. With recent strong growth in the digital lending industry in India, there is a need for industry participants to maintain an ethical code of conduct in order to prevent the rise of unwanted practices that could cause harm to the industry by reducing the confidence of customers, regulators and other market participants.
<br><br>
FACE being an organization striving for betterment of Fintech Industry has decided along with its members to adopt and implement this Ethical Code of Conduct. This code will create common safeguards regarding customer interest and provide a proper solution to the risk created hence bringing in a transparent and fair business practices.
<br><br><b>
APPLICABILITY
</b>
2.1 	The Code of Conduct is a set of principles, and guidelines that are binding on every member (“Member”) in order to make sure there is a uniform, ethical behavior displayed by the members.
<br>
2.2 	This Code of Conduct is applicable to Members in all of their dealings, relations, and transactions with any individual, person or business (“customer”) to whom any financial product or service is offered or provided through the use of any online technology.
<br>
2.3 	This Code of Conduct is in addition to all laws and regulations applicable for lending businesses, including all current regulations and directions issued by the Reserve Bank of India, and by no means aims to supersede any applicable laws or regulatory guidance. In case of any conflict or discrepancy between this Code of Conduct and any law or regulation, that law or regulation will supersede.
<br>
2.4 	The Code of Conduct is subject to change by FACE from time to time.
<br>
2.5 	Members will have to comply with this Code of Conduct and any non-compliance may result in Sanctions as detailed below or as decided by the governing council of FACE.
<br><br>
<b>
ADHERENCE & SUPERVISION
</b><br><br>
3.1 	Members agree, in writing, at the time of membership and renewal of membership, or during the term of membership to adhere to this Code of Conduct.
<br>
3.2 	Members will have to provide, in writing, their acceptance of this Code of Conduct for continuation of their membership.
<br>
3.3 	Each Member, as part of its acceptance of the Code of Conduct, accepts and consents to FACE’s right to apply and give effect to the Sanctions as set out below.
<br>
3.4 	Each Member shall provide to FACE the name of a person in its organization who will be Single Point of Contact (SPOC) regarding this Code of Conduct (“Designated Officer”). The Designated Officer will also be responsible for dissemination of this Code of Conduct (as updated from time to time) within the Member’s organisation at frequent intervals. The name of the Designated Officer will be kept on record of FACE and will be the contact point for all communications with the Member in relation to the Code of Conduct.
<br>
3.5 	Each Member must submit an annual confirmation to the Management Committee, in writing, on its compliance with the Code of Conduct, in such form as the FACE may require from time to time (“Yearly Submission”). The Yearly Submission will include a certification by a director, company secretary or other key managerial personnel that it is in compliance with this Code of Conduct. The Yearly Submission will be required from each Member in order to continue its membership and participation in FACE activities.
<br>
3.6 	Members can conduct regular training session for staff, agent and representatives on this Code of Conduct and update them regarding the effects of non-compliance.
<br><br>
<b>
PROMOTION OF THE CODE OF CONDUCT
</b><br><br>
4.1 	Members should spread awareness about this Code of Conduct.
<br>
4.2 	Members should display the Code of Conduct as part of their fair practices code (similar to Banks and NBFCs) and should make it be available to customers on the Members Website or App.
<br>
<br><b>
TENETS OF THE CODE OF CONDUCT
</b><br><br>
OBLIGATION TO ADHERE TO ALL APPLICABLE LAWS AND REGULATIONS
<br>
1.1 	Each Member undertakes to comply with all provisions of all applicable laws and regulations, including, but not limited to:
<br>
Applicable laws and regulations concerning financial services, consumer protection and any general laws including without limitation all directions, guidelines, circulars and notifications issued by the Reserve Bank of India and other relevant statutory, regulatory or government bodies;
<br>
Applicable laws and regulations in the field of communication and informatics related to the protection of personal data in electronic systems;
<br>
Any other applicable law and regulations relating to business, operations and practices of members.
<br><br>
1.2 	All provisions in this Code of Conduct complement and are in addition to the obligations of each Member under laws and regulations applicable to the Member. Each Member is individually and solely responsible for its compliance with applicable laws, regulations and this Code of Conduct.
<br><br>
<b>
BASIC PRINCIPLES
</b><br><br>
2.1 	Transparency with regards to Product, Pricing and Services
<br>
All the members must at all times offer products and services that are clear and unambiguous.
<br>
Members should ensure that all their marketing and advertising material and outreach to customers is not false, ambiguous or deceptive.
<br>
All the members must make every effort to provide relevant information in a format and language easily understandable to customers.
<br>
2.2 	Fair treatment of Customers in Servicing and Collection
<br>
<br>
<b>
Each Member must:
</b><br><br>
have board approved fair practices code and ensure that the code is complied with;
<br>
adopt ethical practices across its product and operations that treat their customers with dignity and respect and not resort to any harassment or intimidation;
<br>
ensure that employees, staff, representatives and agents are adequately and regularly trained to deal with the customers in an appropriate manner to give effect to the above.
<br>
ensure that employees, staff, representatives and agents treat all the customer fairly and according to guidelines published by the regulators.
<br>
2.2 	Lending within appropriate affordability limits
<br>
It would be members responsibility to make fair income and affordability assessments of customer and ensure that financial product and services, including the loan and all charges and fees, are not in excess of a customer’s capacity to pay.
<br><br>
<b>
SPECIFIC GUIDANCE
</b><br><br>
3.1 	Product Transparency
<br>
Disclosure of terms, conditions and obligations to customer
<br>
The Member should strive to make sure that detailed terms and conditions of the financial product and services offered are made available to the customer.
<br>
A loan agreement containing detailed outlines of the obligations and commitments of the customer and commitments of the lender in the relationship should be provided to the customer.
<br>
The Customer must be clearly communicated that a loan is being granted which is to be repaid in certain time frame, failing to do so will have consequences in terms of Credit Bureau reporting and potential legal action on the event of default.
<br>
Efforts should be taken to make customer understand the ultimate lender and who would be the Technology Partner. In the event the Member is not the lender, the customer should understand the role and responsibility of the Member in the process and transaction relating to the financial product and services.
<br>
The customer must be clearly communicated the Grievance Redressal Mechanism including forums he can approach.
<br>
Disclosure of all Costs to Customers
<br>
The Member must unambiguously list all costs and fees related to the product or service offered, including all upfront fees, processing fees, interest costs, insurance costs, registration fees, provisions, re-arrangement fees, late fees, prepayment fees or penalties and any other costs charged to the customer.
<br>
The description of all costs, including any conditional or default costs and expenses, must be explicitly provided in a manner that any customer can easily understand.
<br>
The Member must provide examples of the costs to the customer, including any contingent or default costs, in INR format specific to the financial service or product offered, so that the customer understands exactly all such costs.
<br>
The Member must provide a repayment schedule with detailed repayment information and due dates in a clear manner.
<br>
3.2 	Pricing and Cost of Credit
<br>
Adverse Pricing
<br>
Members will not design pricing models that could ever be considered “adverse” or including but not limited to:
Any misleading element in the presentation of costs or fees;
Confusing or complex Cost or fee structures that are designed to extract maximum revenue without consideration of customer understanding.
<br>
Interest Rates
<br>
Members must disclose upfront effective annual interest rate for the loan in the loan documentation alongside loan amount and tenure so that customers can reasonably calculate the cost of credit.
<br>
Late Fees & Fines
<br>
The Member must provide clear information related to the amount and mechanism of imposing fines in the event of a delay. Such information should be disclosed upfront to the customer on or before the conclusion of the contract for financial product or service.
Late payment penalties should be reasonable and should not compound and shall be charged only in accordance with the disclosed policies of the Member.
<br>
Affordability
<br>
The Member must have a system and process of verification and assessment of the financial condition of the customer to assess the eligibility and suitability for the loan or other financial product offered.
The Member must have a system to ensure the accuracy of the data and information provided by a customer on the best effort basis.
<br>
3.3 	Collections and Servicing
<br>
Information about an Ongoing loan
<br>
The Member must provide timely information about loan payments due and outstanding loan amounts in a format that the customer clearly understands.
<br>
Collection Process
<br>
The Member will ensure that during the collection process there is no harassment or intimidation.
The Member will ensure that its staff, agents and representatives are adequately trained to deal with the customers in an appropriate manner and are not rude or humiliating in their dealings with the customer.
<br>
3.4 	Data Usage and Sharing
<br>
The Member must follow a consent-based architecture for data capture with informed consent provided by the customer following a detailed explanation of the data being captured and used (including sharing of such data with third parties). The Member shall preserve such digital records of customer consent(s) as proof of informed consent.
<br>
The Member is required to practice good faith in the collection, storage, use and sharing of personal data of customers.
<br>
Without limiting the generality of the above, Members shall not:
<br>
intentionally request personal data from a customer even though there is no service that can be provided to a customer;
<br>
intentionally collect personal data irrelevant to the services that will be provided to the customer by the Member;
<br>
collect personal data outside the data that has been agreed to be given by the customer;
<br>
use personal data for purposes that have not been notified or purposes that are different from what was previously notified to the customer;
<br>
collect and store customer’s personal data even though the Member or any person authorized by the Member to collect, process or store such personal data does not yet have a reliable system or processes to carry out such activities or protect such data;
<br>
sell personal data of customers to third parties without explicit consent from the customer;
<br>
share such personal data with third parties other than for purposes consented to by the customer;
<br>
use the personal data in any manner which is likely to cause physical harm or injury to any customer, their family member or any person associated with the customer.
<br>
CUSTOMER GRIEVANCE MANAGEMENT
<br>
Every Member must have a board approved policy for addressing customer complaints in a fair, and prompt manner and shall put into place an efficient mechanism for implementation of such customer complaint policy and for resolution of customer complaints in compliance with rights of the customer under law and contract.
<br>
Each Member shall provide to customers, including by publishing on its website, details of how the customer can contact customer service / concerned compliance officer at the Member or seek redressal of customer complaints.
<br>
The Member should also provide details of how customers can contact the regulated entities, such as banks and NBFCs, which are involved in providing the relevant financial product or services to the customer.
<br>
The Member should also provide details of the right of the customers to raise complaints in consumer forums, RBI, Financial Ombudsman etc. and guidance on how the customer can approach such authorities.
<br><br>
<b>
SANCTIONS
</b>
<br>
5.1 	FACE shall monitor compliance with the Code of Conduct by Members and the Board of Directors may constitute a Compliance Committee for this purpose, and in such event, all references to the Committee in this section will mean such Compliance Committee.
<br>
5.2 	The Committee will notify procedure for the admission of complaints against any Member for the violation of the Code of Conduct, investigation and determination of violation, and the decision on a Sanctions against the non-compliant Member after giving them a reasonable opportunity to represent themselves in this process.
<br>
5.3 	The decision taken by the Committee on any non-compliance with the Code of Conduct will be binding on the relevant Member.
<br>
5.4 	Any appeal regarding such decision shall be with the Board of Directors of FACE and any aggrieved Member can appeal to the Board of Directors within 30 days of such decision.
<br>
5.5 	Any member found in non-compliance with the Code of Conduct shall be given 30 days to take corrective action, failing which the Committee shall be entitled to take actions as mentioned below.
<br>
5.6 	The Committee will be entitled to any or all of the action against a non-compliant Member as detailed below
<br>
5.6.1 	Cancellation of its FACE Membership;
<br>
5.6.2 	Bar the Member from future membership and/or participating in its event and services of FACE for such a period as the Committee may deem fit;
<br>
5.6.3 	Notify all other Members of the cancellation of the membership, and to also publish the fact of such cancellation on the FACE’s website;
<br>
5.6.4 	Report any serious violation of the Code of Conduct to the appropriate authorities, including but not limited to the Reserve Bank of India;
<br>
5.6.5 	Issue such other appropriate directions for ensuring compliance with this Code of Conduct, including obtaining a binding commitment from the Member to take necessary remedial steps for compliance with the Code of Conduct.
