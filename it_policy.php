        <h2>I. Objective</h2>
        <h3>1. Purpose</h3>
        <p>This policy defines the control requirements surrounding the management of access to information on Company’s computer and communications systems.</p>

        <h3>2. Scope</h3>
        <p>This policy applies to all Company’s computer systems and facilities, with a target audience of Company’s Information Technology employees and partners.</p>

        <h3>3. Policy applied to All the internal Parties</h3>
        <h4>A. Access Control System</h4>
        <ul>
            <li><b>Access Control System – User ID Creation Date</b> - Access control systems must be configured to capture and maintain the creation date for every user ID.</li>
            <li><b>Access Control System – Last Logon Date</b> - Access control systems must be configured to capture and maintain the date and time of the last logon for every user ID.</li>
            <li><b>Access Control System – Last Logoff Date</b> - Access control systems must be configured to capture and maintain the date and time of the last logoff for every user ID.</li>
            <li><b>Access Control System – Password Change Date</b> - Access control systems must be configured to capture and maintain the date and time of the last password change for every user ID.</li>
            <li><b>Access Control System – User ID Expiration Date</b> - Access control systems must be configured to capture and maintain an expiration date or every user ID that represents the last date that the user ID is active for use.</li>
            <li><b>Malfunctioning Access Control</b> - If a computer or network access control system is not functioning properly, it must default to denial of privileges to end-users.</li>
            <li><b>Special Privileged Users</b> - All multi-user computer and network systems must support a special type of user ID, which has broadly-defined system privileges that will enable authorized individuals to change the security state of systems.</li>
            <li><b>Operating System User Authentication</b> - Developers must not construct or install other mechanisms to identify or authenticate the identity of users without the advance permission of Company’s management.</li>
            <li><b>Access Control System Modification</b> - The functionality of all access control systems must not be altered, overridden or bypassed via the introduction of additional code or instructions.</li>
            <li><b>Password Generation Algorithms</b> - All software and files containing formulas, algorithms, and other specifics used in the process of generating passwords or Personal Identification Numbers must be controlled with the most stringent security measures supported by the involved computer system.</li>
            <li><b>Password Retrieval</b> - Computer and communication systems must be designed, tested, and controlled so as to prevent both the retrieval of, and unauthorized use of stored passwords, whether the passwords appear in encrypted or unencrypted form.</li>
            <li><b>Access Control Information in Cookies</b> - Company’s information systems must never store any access control information in cookies deposited on, or stored on, end-user computers.</li>
            <li><b>System Capabilities and Commands</b> - End users must be presented with only the system capabilities and commands that they have privileges to perform.</li>
        </ul>

        <h4>B. Authorization</h4>
        <ul>
            <li><b>Sensitive or Valuable Information Access</b> - Access to Company’s sensitive information must be provided only after express management authorization has been obtained.</li>
            <li><b>Granting Access to Organization Information</b> - Access to Company’s information must always be authorized by a designated owner of such information, and must be limited on a need-to-know basis to a reasonably restricted number of people.</li>
            <li><b>Information System Privilege Usage</b> - Every information system privilege that has not been specifically permitted by the Company’s management must not be employed for any Company’s business purpose until approved in writing.</li>
            <li><b>Granting System Privileges</b> - Computer and communication system privileges must be granted only by a clear chain of authority delegation.</li>
            <li><b>User ID and Privilege Approval</b> - Whenever user IDs, business application system privileges, or system privileges involve capabilities that go beyond those routinely granted to general users, they must be approved in advance by the user’s immediate supervisor and Company’s management.</li>
            <li><b>Owner Approval for Privileges</b> - Prior to being granted to users, business application system privileges must be approved by the applicable information owner.</li>
            <li><b>System Access Request Authorization</b> - All requests for additional privileges on Company’s multi-user systems or networks must be submitted on a completed system access request form that is authorized by the user’s immediate manager.</li>
            <li><b>Default User Privileges</b> - Without specific written approval from management, administrators must not grant any privileges, beyond electronic mail and word processing, to any user.</li>
            <li><b>Computer Access Training</b> - All Company’s users must complete an approved information security training class before they are granted access to any Company’s computer systems.</li>
        </ul>

        <h4>C. Access and Privilege Assignment</h4>
        <ul>
            <li><b>Production Programs and Information Access</b> - Access controls to production programs and information must be configured such that production programs and information systems software support personnel are not granted access privileges except for problem resolution.</li>
            <li><b>Operations Personnel Information Access</b> - Access controls to production programs and information must be such that computer operations personnel are restricted from modifying systems software, application software, and production information.</li>
            <li><b>Privilege Restriction — Need to Know</b> - The computer and communications system privileges of all users, systems, and programs must be restricted based on the need to know.</li>
            <li><b>User IDs Employed in Abusive Activity</b> - All access privileges for a user ID shown to be engaged in abusive or criminal activity must be immediately revoked.</li>
            <li><b>Developer Access To Production Business Information</b> - Where access to production business information is required so that new or modified business application systems may be developed or tested, only “read” and “copy” access must be granted on production machines. This access is permitted only for the duration of the testing and related development efforts, and must be promptly revoked upon the successful completion of these efforts.</li>
            <li><b>Secret Information Access</b> - Access to sensitive information must be granted only to specific individuals, not groups of individuals.</li>
        </ul>
