        <h2>II. PASSWORDS</h2>
        <h3>1. Introduction</h3>
        <p>Passwords are an important aspect of computer security. They are the front line of protection for user accounts. A poorly chosen password may result in a compromise of Company’s entire network. As such, all employees are responsible for taking the appropriate steps, as outlined below, to select and secure their password.</p>

        <h3>2. Purpose</h3>
        <p>The purpose of this policy is to establish a standard for the creation of strong passwords, the protection of those passwords, and the frequency of change.</p>

        <h3>3. Policy</h3>
        <h4>A. General</h4>
        <ul>
            <li>All application-level passwords (e.g. Passwords used to logging into .net applications and web module) must be changed at least every 45 days and cannot be used the past 5 password.</li>
            <li>All user-level passwords (e.g., email, desktop computer, etc.) must be changed at least every 45 days and cannot be reused the past 10 passwords.</li>
            <li>Passwords must not be inserted into email messages or other forms of electronic communication.</li>
            <li>All user-level and application-level passwords must conform to the guidelines described below.</li>
        </ul>

        <h4>B. Guidelines</h4>
        <b>The Password Construction Requirements</b>
        <ol type="i">
            <li>Be a minimum length of eight (8) characters on all systems and maximum of twelve (12) characters.</li>
            <li>Not be a dictionary word or proper name.</li>
            <li>Not be the same as the User ID.</li>
            <li>Expire within a maximum of 45 calendar days.</li>
            <li>Password must have at least one alphabet and one numeric character and one special character.</li>
            <li>Not be identical to the previous five (5) passwords.</li>
            <li>Not be displayed when entered.</li>
            <li>Ensure passwords are only reset for authorized user.</li>
            <li>Not be transmitted in the clear or plaintext outside the secure location.</li>
        </ol>

        <h4>a. Password Deletion</h4>
        <p>All passwords that are no longer needed must be deleted or disabled immediately. When a user quits (retires, resigned, suspended, dismissed, etc.), Default passwords shall be changed immediately on all equipment or the user id must be disabled immediately.</p>

        <h4>b. Password Protection Standards</h4>
        <p>Do not use your User ID as your password. Do not share passwords with anyone. All passwords are to be treated as sensitive and confidential information.</p>
        <p>Here is a list of “do not’s”</p>
        <ul>
            <li>Don’t reveal a password over the phone to anyone</li>
            <li>Don’t reveal a password in an mail message</li>
            <li>Don’t reveal a password to the boss</li>
            <li>Don’ talk about a password in front of others</li>
            <li>Don’t hint at the format of a password (e.g., “my family name”)</li>
            <li>Don’t reveal a password on questionnaires or security forms</li>
            <li>Don’t share a password with family members</li>
            <li>Don’t reveal a password to a co-worker while on vacation</li>
            <li>Don’t use the "Remember Password" feature of applications</li>
            <li>Don’t write passwords down and store them anywhere in your office.</li>
            <li>Don’t store passwords in a file on ANY computer system unencrypted.</li>
        </ul>
        <p>If someone demands a password, refer them to this document or have them call COMPANY help desk.</p>
        <p>If an account or password is suspected to have been compromised, report the incident to COMPANY and change all passwords.</p>

        <h4>c. Application Development Standards</h4>
        <p>Application developers must ensure their programs contain the following security precautions:</p>
        <ul>
            <li>Should support authentication of individual users, not groups.</li>
            <li>Should not store passwords in clear text or in any easily reversible form.</li>
            <li>Copy paste of user id and password should be disabled in application.</li>
            <li>System should compel the user to change his password when he logs in for the first time.</li>
            <li>System should disable the user id, if wrong password is entered on three consecutive occasions.</li>
        </ul>

        <h4>d. Remote Access Users</h4>
        <p>Access to the COMPANY networks via remote access is to be controlled by using either a Virtual Private Network (in which a password and user id are required).</p>
